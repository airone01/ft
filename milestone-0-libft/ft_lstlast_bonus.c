#include "libft.h"

/**
 * Returns the last node of the list.
 * @param	lst	The beginning of the list
 * @returns     The last node of the list
 */
t_list	*ft_lstlast(t_list *lst)
{
	if (!lst)
		return (0);
	while (lst->next)
		lst = lst->next;
	return (lst);
}
