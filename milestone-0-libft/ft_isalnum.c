#include "libft.h"

/*
 * Tells if a character is alphanumerical (lowercase or uppercase or numerical
 * digit)
 *
 * @param	c	character
 *
 * @returns	boolean result
 */
int	ft_isalnum(int c)
{
	return (ft_isalpha(c) || ft_isdigit(c));
}
