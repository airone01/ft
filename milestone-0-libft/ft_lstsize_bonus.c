#include "libft.h"

/**
 * Counts the number of nodes in a list.
 * @param	lst	The beginning of the list
 * @returns     The length of the list
 */
int	ft_lstsize(t_list *lst)
{
	size_t	i;

	i = 0;
	while (lst)
	{
		lst = lst->next;
		i++;
	}
	return (i);
}
