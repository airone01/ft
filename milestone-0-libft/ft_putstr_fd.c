#include "libft.h"

/*
 * Outputs the string ’s’ to the given file descriptor.
 *
 * @param	s	the string to output
 * @param	fd	the file descriptor on which to write
 */
void	ft_putstr_fd(char *s, int fd)
{
	write(fd, s, ft_strlen(s) * sizeof(char));
}
