#include "libft.h"

/**
 * Gets the length of a string.
 * @param	str	String to get length from
 * @retuns      Length of str
 */
size_t	ft_strlen(const char *str)
{
	size_t	i;

	i = 0;
	while (str[i] != '\0')
		i++;
	return (i);
}
