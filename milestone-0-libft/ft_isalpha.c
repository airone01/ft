/**
 * Tells if a character is alphabetical (lowercase or uppercase)
 * @param	c	    Character
 * @returns	        Boolean result
 */
int	ft_isalpha(int c)
{
	return ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'));
}
